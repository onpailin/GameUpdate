void Player::setWall() {

	for (int y = 0; y < this->tiles.size(); y++) 
	{
		for(int x = 0; x < this->tiles.at(y).size(); x++)
		{
			if (this->tiles.at(y).at(x) == 1)
			{
				RectangleShape wall;
				wall.setFillColor(Color(255, 255, 255));
				wall.setSize(Vector2f(16.f, 16.f));
				wall.setPosition(16.f * x, 16.f * y);
				this->walls.push_back(wall);
			}
		}
	}
}

void Player::drawWall(RenderTarget* target) {

	for(auto wall: this->walls) {

		target->draw(wall);
	}
}

