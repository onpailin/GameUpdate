void Player::spawnCoins(RenderTarget* target) {
	
	int iterate = 0;
	while(true) {
		int x = rand() % target->getSize().x;
		int y = rand() % target->getSize().y;
		bool can_spawn = true;

		Coins coin_spawn(x, y, &this->coin_texture);

		for (int i = 0; i < this->walls.size(); i++)
		{	
			if(x > this->walls.at(i).getPosition().x - 16 && x < this->walls.at(i).getPosition().x + 16 &&
				y > this->walls.at(i).getPosition().y - 16 && y < this->walls.at(i).getPosition().y + 16) {
				can_spawn = false;
			}
		}
		if (iterate > 100) return;
		if(!can_spawn) continue;
		this->coins.push_back(coin_spawn);
		return;
	}
}

void Player::render(RenderTarget* target)
{
	// Spawn Coins
	if(this->coins_delay < 0) {
		this->coins_delay = 180;
		this->spawnCoins(target);
	}
	this->coins_delay --;

	//this->drawWall(target); //<< ไว้วาดกำแพงหลอก
	target->draw(this->sprite);
	
	for(int i = 0; i < this->coins.size(); i++) {
		this->coins.at(i).rendercoins(target);
	}
} 

void Player::updatePhysics()
{
	this->velocity.y += 2 * this->gravity;
	this->velocity *= this->drag;

	//Limit deceleration
	if (abs(this->velocity.x) < this->velocityMin)
		this->velocity.x = 0.f;
	this->sprite.move(this->velocity);
}

