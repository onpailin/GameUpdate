void Player::updateAnimation()
{
	if (this->standNum >= 3)
	{
		this->standNum = 1;
	}
	if (this->moveNum >= 5)
	{
		this->moveNum = 1;
	}
	if (this->aniTime.getElapsedTime().asSeconds() >= 0.1f)
	{
		if (this->moving == false)
		{
			this->moveNum = 1;
			this->preHeight = 33.f;
			this->width = 39.f;
			switch (this->standNum)
			{
			case 1: this->preWidth = 82.f;
				break;
			case 2: this->preWidth = 33.f;
				break;
			default:
				break;
			}
			this->standNum++;
		}
		else if (this->moving == true)
		{
			this->standNum = 3;
			this->preHeight = 135.f;
			this->width = 38.f;
			switch (this->moveNum)
			{
			case 1: this->preWidth = 24.f;
				break;
			case 2: this->preWidth = 83.f;
				break;
			case 3: this->preWidth = 308.f;
				break;
			case 4: this->preWidth = 356.f;
				break;
			default:
				break;
			}
			this->moveNum++;
		}
		if (this->turnLeft == false)
		{
			this->currentFrame = IntRect(this->preWidth, this->preHeight, this->width, this->height);
		}
		else if (this->turnLeft == true)
		{
			this->currentFrame = IntRect(this->preWidth + this->width, this->preHeight, -this->width, this->height);
		}
		this->sprite.setTextureRect(this->currentFrame);
		this->aniTime.restart();
	}
}

void Player::update(RenderTarget* target, float deltaTime)
{
	this->updateInput(deltaTime);
	this->updateMaps();
	this->updateAnimation();
	this->updateCollision();
	for(int i = 0; i < this->coins.size(); i++) {
		this->coins.at(i).updatecoins();
	}
}

void Player::updateCollision() {

	for (auto& wall : this->walls)
	{
		FloatRect playerBounds = this->sprite.getGlobalBounds();
		FloatRect wallBounds = wall.getGlobalBounds();

		FloatRect nextPos = playerBounds;

		if (wallBounds.intersects(nextPos))
		{
			//right
			if (playerBounds.left < wallBounds.left
				&& playerBounds.left + playerBounds.width < wallBounds.left + wallBounds.width
				&& playerBounds.top < wallBounds.top + wallBounds.height
				&& playerBounds.top + playerBounds.height > wallBounds.top)
			{
				//movementSpeed = 0.f;
				sprite.setPosition(wallBounds.left - playerBounds.width, playerBounds.top);
			}
			//left
			else if (playerBounds.left > wallBounds.left
				&& playerBounds.left + playerBounds.width > wallBounds.left + wallBounds.width
				&& playerBounds.top < wallBounds.top + wallBounds.height
				&& playerBounds.top + playerBounds.height > wallBounds.top)
			{
				//movementSpeed = 0.f;
				sprite.setPosition(wallBounds.left + wallBounds.width, playerBounds.top);
			}
			//bottom
			if (playerBounds.top < wallBounds.top
				&& playerBounds.top + playerBounds.height < wallBounds.top + wallBounds.height
				&& playerBounds.left < wallBounds.left + wallBounds.width
				&& playerBounds.left + playerBounds.width > wallBounds.left)
			{
				//movementSpeed = 0.f;
				sprite.setPosition(playerBounds.left, wallBounds.top - playerBounds.height);
			}
			//top
			else if (playerBounds.top > wallBounds.top
				&& playerBounds.top + playerBounds.height > wallBounds.top + wallBounds.height
				&& playerBounds.left < wallBounds.left + wallBounds.width
				&& playerBounds.left + playerBounds.width > wallBounds.left)
			{
				//movementSpeed = 0.f;
				sprite.setPosition(playerBounds.left, wallBounds.top + wallBounds.height);
			}
		}
	}
}

